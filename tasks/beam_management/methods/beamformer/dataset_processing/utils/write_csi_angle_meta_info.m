function write_csi_angle_meta_info(sqlite_conn, csi_path, angle_this_channel, tilt_info)
    % 确保表存在
    exec(sqlite_conn, ['CREATE TABLE IF NOT EXISTS csi_meta_info (' ...
                       'csi_path TEXT, ' ...
                       'aod_phi REAL, aod_theta REAL, ' ...
                       'aoa_phi REAL, aoa_theta REAL, ' ...
                       'tx_about_y REAL, tx_about_z REAL, ' ...
                       'rx_about_y REAL, rx_about_z REAL)']);

    % 提取角度信息
    aod_phi = angle_this_channel.aod_phi;
    aod_theta = angle_this_channel.aod_theta;
    aoa_phi = angle_this_channel.aoa_phi;
    aoa_theta = angle_this_channel.aoa_theta;

    % 提取 tilt 信息并转换为 degrees
    tx_about_y = rad2deg(tilt_info.tx_about_y);
    tx_about_z = rad2deg(tilt_info.tx_about_z);
    rx_about_y = rad2deg(tilt_info.rx_about_y);
    rx_about_z = rad2deg(tilt_info.rx_about_z);

    % 构造 SQL 插入语句
    sql_query = sprintf(['INSERT INTO csi_meta_info ' ...
                         '(csi_path, aod_phi, aod_theta, aoa_phi, aoa_theta, ' ...
                         'tx_about_y, tx_about_z, rx_about_y, rx_about_z) ' ...
                         'VALUES (''%s'', %.6f, %.6f, %.6f, %.6f, %.6f, %.6f, %.6f, %.6f);'], ...
                         csi_path, aod_phi, aod_theta, aoa_phi, aoa_theta, ...
                         tx_about_y, tx_about_z, rx_about_y, rx_about_z);

    % 执行插入
    exec(sqlite_conn, sql_query);
end
